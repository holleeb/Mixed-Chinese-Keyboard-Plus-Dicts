function mapHkscsPua2Unicode($str) {
	/*
	Copyright (c) 2015 Gnought <gnought@gmail.com>
	Licensed in GPL
	
	HKSCS PUA Mapping [HKSCS-2001 => HKSCS-2004]
	Lots of Hong Kong characters are defined inside the PUA segment (E000 - F8FF) for compatibility in 
	previous ISO/IEC 10646:2001 (HKSCS-2001) standard, but their code points are assigned to the 
	Unicode SIP segment (U+20000  U+2FFFF) in ISO/IEC 10646:2003 (HKSCS-2004) standard, which conforms to Unicode 4.1.
	http://www.ogcio.gov.hk/en/business/tech_promotion/ccli/terms/doc/e_hkscs_2008.pdf
	https://en.wikipedia.org/wiki/Unicode
	http://hkitf.org.hk/wp-content/uploads/2010/07/HKSCS-2004-Support-for-Windows-Platform-v1.0.pdf
	*/
	//$str = mb_convert_encoding(str_replace("\"", "", preg_replace_callback("/\\&\\#([0-9]{3,10})\\;/", function($r) { return html_entity_decode("&#".$r[1].";"); }, $str)), 'HTML-ENTITIES', "utf-8");
	$ncr = mb_convert_encoding($str, 'HTML-ENTITIES', 'UTF-8');
	$ncr_utf8 = preg_replace_callback('/&#(x[0-9A-Za-z]{4}|\d{5,6});/', 
		function ($matches){
			global $hkscspua2uni;
			$puacode = strtolower($matches[1]);
			$puacode = preg_match('/^x/', $puacode) ? preg_replace('/^x/', '', $puacode) : dechex($puacode);
			if(!array_key_exists($puacode,$hkscspua2uni)) return '&#x'.$puacode.';';
			$val = $hkscspua2uni[$puacode];
			return '&#x'.(is_array($val) ? (count($val) === 1 ?  $val[0].';' : $val[0].';&#x'.$val[1]) : $val).';';
		}, $ncr
	);
	return html_entity_decode($ncr_utf8, ENT_QUOTES, 'UTF-8');
}
